function MouseOver(elem) {
	elem.style.width = "105%";
	elem.style.height = "105%";
	elem.style.border = "2px #808080 solid";
	//#808080 is the color grey
}

function MouseOut(elem) {
	elem.style.width = "100%";
	elem.style.height = "100%";
	elem.style.border = "1px #4169E1 solid";
	//#4169E1 is some blue color
}



